def manhattanHeuristic(position, problem, info={}):
    "The Manhattan distance heuristic for a PositionSearchProblem"
    xy1 = position
    xy2 = problem.goal
    return abs(xy1[0] - xy2[0]) + abs(xy1[1] - xy2[1])

# This heuristic is to be used with bidirectional search
# The standard manhatan heuristic could not be used because it
# only takes into account forward search.
# If the function is called while expanding nodes in the forward direction
# then the goal is the food square, if it is called while expanding nodes in the
# backward direction the goal becomes the starting state
def manhattanHeuristicBDS(position, problem, search_type, info={}):
    "The Manhattan distance heuristic for a PositionSearchProblem using BDS"
    # Forward search
    if (search_type == problem.getStartState()):
        xy1 = position
        xy2 = problem.goal
        return abs(xy1[0] - xy2[0]) + abs(xy1[1] - xy2[1])
    # Backward search
    else:
        xy1 = position
        xy2 = problem.getStartState()
        return abs(xy1[0] - xy2[0]) + abs(xy1[1] - xy2[1])

def euclideanHeuristic(position, problem, info={}):
    "The Euclidean distance heuristic for a PositionSearchProblem"
    xy1 = position
    xy2 = problem.goal
    return ( (xy1[0] - xy2[0]) ** 2 + (xy1[1] - xy2[1]) ** 2 ) ** 0.5

